class ReflectionHelper(object):
    """
    Provides reflection related services
    """

    @staticmethod
    def getClass(clazz):
        """
        Fetches class according to it path
        
        @param clazz: the class path
        
        @return: Class
        """
        parts = clazz.split('.')
        module = ".".join(parts[:-1])
        m = __import__(module)
        for comp in parts[1:]:
            m = getattr(m, comp)
        return m
